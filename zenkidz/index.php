            		<h1 class="main-heading size-130">ZENKIDZ</h1>
            		<p>ZenKidz é uma atividade baseada na ArtZen, mas desenvolvida especialmente para as crianças. Trata-se do <em>meditative doodle</em> com o objetivo de inspirar os pequenos a focarem no momento presente e se concentrarem em uma atividade, além de despertar o gosto pelo desenho e pela arte.</p>

                    <p>O primeiro passo para a cerimônia de ZenKidz é simplesmente relaxar e respirar profundamente, buscando atenção para a atividade. As crianças trabalham a partir de formas e rabiscos predeterminados, mas que permitem que, sozinhas e focadas, encontrem seus caminhos para um desenho único e pessoal.</p>

